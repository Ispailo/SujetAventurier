package exercice.aventurier;

public class Aventurier {
    int x;
    int y;

    public Aventurier() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
